package Java_Programs._16_ForLoop;

public class P051_For_Loop_Diff_Var {
    public static void main(String[] args) {
        for (int anuj=0;anuj<10;anuj++){ //11 times
            System.out.println(anuj);
            //this is valid
        }

        for (int _1=0;_1<10;_1++){ //11 times
            System.out.println(_1);
            //this is valid
        }
    }
}
